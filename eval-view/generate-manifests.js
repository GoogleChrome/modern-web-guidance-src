import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import matter from 'gray-matter';

const resultsDir = path.resolve('../harness/results');
const guidesDir = path.resolve('../guides');

/** 
 * Scans guides for web-feature-ids in frontmatter and generates window.__featuresMapping.
 */
export function generateMapping(outputDir = '.') {
    const mapping = {}; // usecase -> [feature_ids]

    function scanGuides(dir) {
        if (!fs.existsSync(dir)) return;
        const items = fs.readdirSync(dir, { withFileTypes: true });
        for (const item of items) {
            if (item.isDirectory()) {
                const subDir = path.join(dir, item.name);
                const guidePath = path.join(subDir, 'guide.md');
                if (fs.existsSync(guidePath)) {
                    const text = fs.readFileSync(guidePath, 'utf8');
                    const parsed = matter(text);
                    if (parsed.data && parsed.data['web-feature-ids']) {
                        const features = parsed.data['web-feature-ids'];
                        if (Array.isArray(features) && features.length > 0) {
                            mapping[item.name] = features;
                        }
                    }
                }
                scanGuides(subDir); // recurse
            }
        }
    }

    if (fs.existsSync(guidesDir)) {
        scanGuides(guidesDir);
    }

    const outputFilePath = path.join(outputDir, 'features_mapping.gen.js');
    const jsContent = `// Automatically generated by generate-manifests.js. Do not edit.\nwindow.__featuresMapping = ${JSON.stringify(mapping, null, 2)};\n`;
    fs.writeFileSync(outputFilePath, jsContent);
    return mapping;
}

/** 
 * Generates suites.gen.json based on folders in harness/results that contain evals.json.
 */
export function generateSuitesManifest(outputDir = '.') {
    const suites = [];
    if (!fs.existsSync(resultsDir)) return [];

    const items = fs.readdirSync(resultsDir, { withFileTypes: true });
    for (const item of items) {
        if (item.isDirectory()) {
            const suiteDir = path.join(resultsDir, item.name);
            if (fs.existsSync(path.join(suiteDir, 'evals.json'))) {
                suites.push(item.name);
            }
        }
    }
    suites.sort();
    const outputPath = path.join(outputDir, 'suites.gen.json');
    fs.writeFileSync(outputPath, JSON.stringify(suites, null, 2));
    return suites;
}

/** 
 * Generates grouped-tasks.gen.json by scanning guides.
 */
export async function generateGroupedTasksManifest(outputDir = '.') {
    const { getTaskMap } = await import('../lib/guide-validation.ts');
    const { USE_CASES } = await import('../serving/lib/practices.ts');
    const taskMap = getTaskMap();
    const grouped = {};
    const disciplines = {};
    
    for (const [key, info] of taskMap.entries()) {
        const [guide, task] = key.split('/');
        const parentDir = path.basename(path.dirname(info.guideDir));
        const isSkill = parentDir === 'guides';
        
        if (isSkill) {
            if (!disciplines[guide]) disciplines[guide] = [];
            disciplines[guide].push(task);
        } else {
            const useCase = USE_CASES.find(u => u.id === guide);
            const category = useCase ? useCase.category : 'Uncategorized';
            if (!grouped[category]) grouped[category] = {};
            if (!grouped[category][guide]) grouped[category][guide] = [];
            grouped[category][guide].push(task);
        }
    }
    
    const outputPath = path.join(outputDir, 'grouped-tasks.gen.json');
    fs.writeFileSync(outputPath, JSON.stringify({ guides: grouped, disciplines: disciplines }, null, 2));
}

/**
 * Main CLI Orchestrator
 */
export async function runAllManifests({ resultsOnly = false, outputDir = '.' } = {}) {
    if (!resultsOnly) {
        console.log('🔄 Generating features mapping...');
        generateMapping(outputDir);
        console.log('🔄 Generating grouped tasks manifest...');
        await generateGroupedTasksManifest(outputDir);
    }

    console.log('🔄 Generating suites manifests...');
    generateSuitesManifest(outputDir);
}

// CLI Runner
const isMain = process.argv[1] && fs.realpathSync(process.argv[1]) === fs.realpathSync(fileURLToPath(import.meta.url));
if (isMain) {
    const args = process.argv.slice(2);
    const resultsOnly = args.includes('--results-only');
    try {
        await runAllManifests({ resultsOnly });
        console.log('✅ All manifests generated successfully.');
    } catch (e) {
        console.error('❌ Error generating manifests:', e);
        process.exit(1);
    }
}
